    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        try {
            if (loginForPOS(jTextFieldUserName.getText(), jPasswordFieldPassword.getText())) {
                systemUser = getSystemUser(jTextFieldUserName.getText());
                
                //pull company information from Islander Furniture
                company=getCompanySingleResult();

                if (systemUser != null) {
                    JOptionPane.showMessageDialog(this, "Welcome " + ", you have login successfully!", "Login Successful", JOptionPane.INFORMATION_MESSAGE);

                    Main main=new Main(this, systemUser,company);
                    main.setVisible(true);
                    main.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    
                    
                    jTextFieldUserName.setText("");                    
                    jPasswordFieldPassword.setText("");

                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Unable to retrieve account information", "Login Error", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Invalid credential!", "Login Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "An unknown error has occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

