    private static boolean loginForPOS(java.lang.String userName, java.lang.String password) {
        Login.LoginPOS_Service service = new Login.LoginPOS_Service();
        Login.LoginPOS port = service.getLoginPOSPort();
        return port.loginForPOS(userName, password);
    }

    private static SystemUser getSystemUser(java.lang.String userName) {
        Login.LoginPOS_Service service = new Login.LoginPOS_Service();
        Login.LoginPOS port = service.getLoginPOSPort();
        return port.getSystemUser(userName);
    }

    private static Company getCompanySingleResult() {
        storeInfo.GetStoreInfoPOS_Service service = new storeInfo.GetStoreInfoPOS_Service();
        storeInfo.GetStoreInfoPOS port = service.getGetStoreInfoPOSPort();
        return port.getCompanySingleResult();
    }

    

  





   

