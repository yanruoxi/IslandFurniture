package entity;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.Collection;
import java.util.HashSet;
import java.util.Set;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.OneToMany;
import javax.persistence.OneToOne;

/**
 *
 * @author Meiling
 */
@Entity
public class Supplier implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long supplierId;
    private String supplierName;
    private String supplierAddress;
    private String telephoneNum;
    private String contactPersonName;
    private String mobileNum;
    private String faxNum;
    private String supplierEmailAddr;
    @OneToMany(mappedBy="supplier")
    private Collection<Contract> contracts = new ArrayList<Contract>();
    @ManyToMany
    private Set<Part> parts = new HashSet<Part>();
    private String supplierDeleteStatus;
    // Added
    @OneToOne
    private SupplierAcct supplierAcct;
    
    public Long getSupplierId() {
        return supplierId;
    }

    public void setSupplierId(Long supplierId) {
        this.supplierId = supplierId;
    }
    
    public String getSupplierName() {
        return supplierName;
    }

    public void setSupplierName(String supplierName) {
        this.supplierName = supplierName;
    }

    public String getSupplierAddress() {
        return supplierAddress;
    }

    public void setSupplierAddress(String supplierAddress) {
        this.supplierAddress = supplierAddress;
    }

    public String getTelephoneNum() {
        return telephoneNum;
    }

    public void setTelephoneNum(String telephoneNum) {
        this.telephoneNum = telephoneNum;
    }

    public String getContactPersonName() {
        return contactPersonName;
    }

    public void setContactPersonName(String contactPersonName) {
        this.contactPersonName = contactPersonName;
    }

    public String getMobileNum() {
        return mobileNum;
    }

    public void setMobileNum(String mobileNum) {
        this.mobileNum = mobileNum;
    }

    public String getFaxNum() {
        return faxNum;
    }

    public void setFaxNum(String faxNum) {
        this.faxNum = faxNum;
    }

    public String getSupplierEmailAddr() {
        return supplierEmailAddr;
    }

    public void setSupplierEmailAddr(String supplierEmailAddr) {
        this.supplierEmailAddr = supplierEmailAddr;
    }

    public Collection<Contract> getContracts() {
        return contracts;
    }

    public void setContracts(Collection<Contract> contracts) {
        this.contracts = contracts;
    }

    public Set<Part> getParts() {
        return parts;
    }

    public void setParts(Set<Part> parts) {
        this.parts = parts;
    }

    public String getSupplierDeleteStatus() {
        return supplierDeleteStatus;
    }

    public void setSupplierDeleteStatus(String supplierDeleteStatus) {
        this.supplierDeleteStatus = supplierDeleteStatus;
    }
    
    //Added
    public SupplierAcct getSupplierAcct() {
        return supplierAcct;
    }

    public void setSupplierAcct(SupplierAcct supplierAcct) {
        this.supplierAcct = supplierAcct;
    }
    
    @Override
    public int hashCode() {
        int hash = 0;
        hash += (supplierId != null ? supplierId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Supplier)) {
            return false;
        }
        Supplier other = (Supplier) object;
        if ((this.supplierId == null && other.supplierId != null) || (this.supplierId != null && !this.supplierId.equals(other.supplierId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entity.Supplier[ supplierId=" + supplierId + " ]";
    }
    
}
